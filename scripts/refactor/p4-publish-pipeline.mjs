#!/usr/bin/env bun
// P4 — Publish pipeline.
//
// Today: prepare-package-dist.mjs writes packages/foo/dist/package.json with
// dist-pointing paths and semver-resolved deps, but lerna publishes from
// packages/foo/ (not /dist), so the transformed manifest is orphaned.
//
// Fix:
//   1. Create scripts/publish-from-dist.mjs — iterates workspace packages,
//      runs `cd packages/foo/dist && npm publish` (or pack for dry-run).
//   2. Mark the obsolete scripts as deprecated (keep for now, log MANUAL):
//        scripts/restore-workspace-refs.js
//        scripts/replace-workspace-versions.js
//      They'll be deleted in a follow-up commit once the new path is verified.
//   3. Add a `release` / `publish:packages` npm script at the root that calls
//      the new publish-from-dist.

import { existsSync } from "node:fs";
import { join } from "node:path";
import {
  REPO_ROOT,
  Stats,
  makeLogger,
  parseFlags,
  preflight,
  readJson,
  writeFileIfChanged,
  writeJson,
} from "./lib/util.mjs";

const PUBLISH_FROM_DIST_PATH = "scripts/publish-from-dist.mjs";

const PUBLISH_FROM_DIST_SOURCE = `#!/usr/bin/env bun
/**
 * publish-from-dist.mjs
 *
 * Publishes every workspace package from its dist/ subdirectory.
 * dist/package.json is generated by prepare-package-dist.mjs at build time
 * and contains:
 *   - main / types / exports pointing at compiled outputs
 *   - workspace:* deps rewritten to real semver
 *
 * Lerna's default behavior publishes from the package root, which sees the
 * source-of-truth package.json (workspace:* + src/ pointers). This script
 * replaces that step.
 *
 * Usage:
 *   bun scripts/publish-from-dist.mjs             # dry-run
 *   bun scripts/publish-from-dist.mjs --apply     # actual publish
 *   bun scripts/publish-from-dist.mjs --apply --tag beta
 *   bun scripts/publish-from-dist.mjs --apply --filter @elizaos/core,@elizaos/shared
 *   bun scripts/publish-from-dist.mjs --apply --otp 123456
 */

import { execFileSync } from "node:child_process";
import { existsSync, readFileSync, readdirSync, statSync } from "node:fs";
import { join, resolve } from "node:path";
import { dirname } from "node:path";
import { fileURLToPath } from "node:url";

const REPO_ROOT = resolve(dirname(fileURLToPath(import.meta.url)), "..");

function parseArgs() {
  const argv = process.argv.slice(2);
  const flags = {
    apply: argv.includes("--apply"),
    tag: undefined,
    filter: undefined,
    otp: undefined,
  };
  const tagIdx = argv.indexOf("--tag");
  if (tagIdx >= 0) flags.tag = argv[tagIdx + 1];
  const filterIdx = argv.indexOf("--filter");
  if (filterIdx >= 0) flags.filter = argv[filterIdx + 1].split(",").map((s) => s.trim());
  const otpIdx = argv.indexOf("--otp");
  if (otpIdx >= 0) flags.otp = argv[otpIdx + 1];
  return flags;
}

const WORKSPACE_GLOBS = [
  "packages",
  "packages/native-plugins",
  "plugins",
  "cloud/packages",
];

function walkPackages() {
  const out = [];
  for (const glob of WORKSPACE_GLOBS) {
    const base = join(REPO_ROOT, glob);
    if (!existsSync(base)) continue;
    walk(base, 3, out);
  }
  return out;
}

function walk(dir, depth, out) {
  if (depth < 0) return;
  if (!statSync(dir).isDirectory()) return;
  const pkgPath = join(dir, "package.json");
  if (existsSync(pkgPath)) {
    try {
      const pkg = JSON.parse(readFileSync(pkgPath, "utf8"));
      if (pkg.name && pkg.private !== true) {
        out.push({ name: pkg.name, dir, version: pkg.version });
      }
    } catch {}
    return;
  }
  for (const entry of readdirSync(dir, { withFileTypes: true })) {
    if (!entry.isDirectory()) continue;
    if (entry.name === "node_modules" || entry.name === "dist") continue;
    if (entry.name.startsWith(".")) continue;
    walk(join(dir, entry.name), depth - 1, out);
  }
}

function main() {
  const flags = parseArgs();
  const pkgs = walkPackages();
  const filtered = flags.filter
    ? pkgs.filter((p) => flags.filter.includes(p.name))
    : pkgs;

  console.log(\`\${flags.apply ? "[PUBLISH]" : "[DRY-RUN]"} \${filtered.length} packages\`);
  if (flags.tag) console.log(\`  tag: \${flags.tag}\`);
  if (flags.filter) console.log(\`  filter: \${flags.filter.join(", ")}\`);

  let succeeded = 0;
  let failed = 0;
  for (const pkg of filtered) {
    const distDir = join(pkg.dir, "dist");
    if (!existsSync(join(distDir, "package.json"))) {
      console.warn(\`  \${pkg.name}: SKIP (no dist/package.json — did you run build?)\`);
      continue;
    }
    const args = [flags.apply ? "publish" : "pack", "--dry-run"];
    if (flags.apply) args.splice(1, 1); // remove --dry-run for real publish
    if (flags.tag) args.push("--tag", flags.tag);
    if (flags.otp) args.push("--otp", flags.otp);
    args.push("--access", "public");

    try {
      console.log(\`  \${pkg.name}@\${pkg.version}: \${args.join(" ")}\`);
      execFileSync("npm", args, { cwd: distDir, stdio: "inherit" });
      succeeded++;
    } catch (err) {
      failed++;
      console.error(\`    FAILED: \${err.message}\`);
    }
  }

  console.log(\`\\nDone. \${succeeded} succeeded, \${failed} failed.\`);
  if (failed) process.exit(1);
}

main();
`;

async function main() {
  const flags = parseFlags();
  const log = makeLogger(flags);
  preflight("p4-publish-pipeline", flags, log);
  const stats = new Stats();

  log.section("1. Create scripts/publish-from-dist.mjs");
  writeFileIfChanged(
    join(REPO_ROOT, PUBLISH_FROM_DIST_PATH),
    PUBLISH_FROM_DIST_SOURCE,
    flags,
    log,
  );
  stats.incr("publish script created", 1);

  log.section("2. Add release scripts to root package.json");
  const rootPkgPath = join(REPO_ROOT, "package.json");
  const rootPkg = readJson(rootPkgPath);
  rootPkg.scripts = rootPkg.scripts ?? {};
  let scriptsAdded = 0;
  if (!rootPkg.scripts["publish:dry-run"]) {
    rootPkg.scripts["publish:dry-run"] = "bun scripts/publish-from-dist.mjs";
    scriptsAdded++;
  }
  if (!rootPkg.scripts["publish:packages"]) {
    rootPkg.scripts["publish:packages"] = "bun scripts/publish-from-dist.mjs --apply";
    scriptsAdded++;
  }
  if (scriptsAdded > 0) {
    log.info(`add ${scriptsAdded} scripts to root package.json`);
    writeJson(rootPkgPath, rootPkg, flags, log);
    stats.incr("root scripts added", scriptsAdded);
  }

  log.section("3. Mark obsolete scripts");
  const obsolete = [
    "scripts/restore-workspace-refs.js",
    "scripts/replace-workspace-versions.js",
  ];
  for (const path of obsolete) {
    const abs = join(REPO_ROOT, path);
    if (existsSync(abs)) {
      log.manual(
        `${path} is now obsolete (publish reads dist/package.json directly). Delete in a follow-up commit after verifying the new flow.`,
      );
      stats.incr("obsolete scripts flagged");
    }
  }

  log.section("4. Verify prepare-package-dist.mjs idempotence");
  const ppdPath = join(REPO_ROOT, "scripts/prepare-package-dist.mjs");
  if (!existsSync(ppdPath)) {
    log.warn(`scripts/prepare-package-dist.mjs missing — verify publish flow manually`);
  } else {
    log.note(
      "After P3 flips main→dist, prepare-package-dist.mjs may produce dist/dist/... duplications. Re-run with sample package and verify.",
    );
    log.manual(
      "Run: bun scripts/prepare-package-dist.mjs packages/core; cat packages/core/dist/package.json | head -30",
    );
  }

  stats.print(log);
}

main().catch((err) => {
  console.error(err);
  process.exit(1);
});
