#!/usr/bin/env node
// Bake the generated launcher view icons (PNG) into a committed TS file as
// base64 data URIs, so every tile is preloaded in the JS bundle — no network,
// no /api/views hero 404 on native, no client-SVG fallback, no loading/empty
// state. Source PNGs are generated via codex CLI (see scripts/gen-view-icons).
//
// Usage: node scripts/bake-view-icons.mjs [srcDir]   (default /tmp/view-icons)
import { readdirSync, readFileSync, writeFileSync, existsSync } from "node:fs";
import { join, dirname } from "node:path";
import { fileURLToPath } from "node:url";

const here = dirname(fileURLToPath(import.meta.url));
const srcDir = process.argv[2] || "/tmp/view-icons";
const out = join(here, "..", "src", "components", "views", "view-icons.generated.ts");

const files = existsSync(srcDir)
  ? readdirSync(srcDir)
      .filter((f) => f.endsWith(".png"))
      .sort()
  : [];

const entries = files.map((f) => {
  const id = f.slice(0, -4);
  const b64 = readFileSync(join(srcDir, f)).toString("base64");
  return `  ${JSON.stringify(id)}: "data:image/png;base64,${b64}",`;
});

const ts = `// AUTO-GENERATED by scripts/bake-view-icons.mjs — do not edit by hand.
// Bundled launcher view icons as base64 PNG data URIs: every tile is
// preloaded in the JS bundle (no network, no /api hero, no SVG fallback, never
// a loading/empty state). Regenerate the source PNGs with the codex image-gen
// script, then re-run the bake.
/* biome-ignore-all lint: generated data */
export const VIEW_ICONS: Record<string, string> = {
${entries.join("\n")}
};

/** The bundled icon data URI for a view/app id, falling back to the generic
 *  default. Returns "" only if even the default has not been baked yet. */
export function viewIconDataUri(id: string): string {
  return VIEW_ICONS[id] ?? VIEW_ICONS.default ?? "";
}
`;

writeFileSync(out, ts);
console.log(`baked ${files.length} icons -> ${out}`);
