#!/usr/bin/env node
/**
 * Regenerates `known-phrase.ts` — the bundled voice self-test audio fixture, a
 * 16 kHz mono PCM WAV embedded as a base64 `data:` URL so it works in the
 * @elizaos/ui library build and any host bundler with no asset pipeline.
 *
 * NOTE: this clip is a reproducible SYNTHETIC tone (no local TTS engine in this
 * env). It is sufficient for the wiring lanes (the mocked web/ui-smoke ASR
 * route returns EXPECTED_PHRASE regardless of audio content, so it proves real
 * mic capture + WAV encode + POST + the full round-trip + the self-test screen).
 * A genuine real-ASR pass needs a real-speech clip of EXPECTED_PHRASE; swap the
 * synth below for a TTS-rendered/recorded 16 kHz mono PCM and re-run this script.
 *
 *   node packages/ui/src/voice/voice-selftest/fixtures/gen-known-phrase.mjs
 */
import { writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const EXPECTED_PHRASE = "what time is it";
const SAMPLE_RATE = 16000;
const DURATION_SEC = 1.4;

function buildWav() {
  const n = Math.floor(SAMPLE_RATE * DURATION_SEC);
  const pcm = Buffer.alloc(n * 2);
  // A gentle multi-tone glide so the clip is non-trivial, non-empty, and
  // decodes cleanly via AudioContext.decodeAudioData in headless Chromium.
  for (let i = 0; i < n; i += 1) {
    const t = i / SAMPLE_RATE;
    const env = Math.min(1, t / 0.05, (DURATION_SEC - t) / 0.05);
    const s =
      0.5 * Math.sin(2 * Math.PI * (180 + 60 * t) * t) +
      0.3 * Math.sin(2 * Math.PI * 320 * t);
    pcm.writeInt16LE(Math.max(-32767, Math.min(32767, env * s * 30000)), i * 2);
  }
  const header = Buffer.alloc(44);
  header.write("RIFF", 0);
  header.writeUInt32LE(36 + pcm.length, 4);
  header.write("WAVE", 8);
  header.write("fmt ", 12);
  header.writeUInt32LE(16, 16);
  header.writeUInt16LE(1, 20); // PCM
  header.writeUInt16LE(1, 22); // mono
  header.writeUInt32LE(SAMPLE_RATE, 24);
  header.writeUInt32LE(SAMPLE_RATE * 2, 28); // byte rate
  header.writeUInt16LE(2, 32); // block align
  header.writeUInt16LE(16, 34); // bits per sample
  header.write("data", 36);
  header.writeUInt32LE(pcm.length, 40);
  return Buffer.concat([header, pcm]);
}

const wav = buildWav();
const dataUrl = `data:audio/wav;base64,${wav.toString("base64")}`;
const dir = dirname(fileURLToPath(import.meta.url));
const tsOut = join(dir, "known-phrase.ts");
writeFileSync(
  tsOut,
  `// AUTO-GENERATED by gen-known-phrase.mjs — do not edit by hand.\n` +
    `// 16 kHz mono WAV of the voice self-test phrase, embedded as a data: URL.\n` +
    `export const EXPECTED_PHRASE = ${JSON.stringify(EXPECTED_PHRASE)};\n` +
    `export const KNOWN_PHRASE_WAV_DATA_URL =\n  ${JSON.stringify(dataUrl)};\n`,
);
// Also emit the raw .wav for Chromium --use-file-for-fake-audio-capture (the
// mic-capture / button-press real-audio e2e needs a file on disk, not a URL).
const wavOut = join(dir, "known-phrase.wav");
writeFileSync(wavOut, wav);
console.log(
  `wrote ${tsOut} + ${wavOut} (${wav.length} bytes wav, phrase: "${EXPECTED_PHRASE}")`,
);
