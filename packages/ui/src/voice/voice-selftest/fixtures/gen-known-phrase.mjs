#!/usr/bin/env node
/**
 * Regenerates `known-phrase.ts` from the committed `known-phrase.wav` — the
 * bundled voice self-test audio fixture, embedded as a base64 `data:` URL so it
 * works in the @elizaos/ui library build and any host bundler with no asset
 * pipeline.
 *
 * `known-phrase.wav` is a committed REAL-SPEECH 16 kHz mono clip of
 * EXPECTED_PHRASE, so a genuine real-audio -> real-ASR pass works on any host
 * with a provisioned ASR (e.g. the on-device Android agent). It was originally
 * TTS-synthesized and downsampled to 16 kHz; the clip is now a plain committed
 * fixture with no build-time dependency. After replacing the WAV, re-embed it:
 *
 *   node packages/ui/src/voice/voice-selftest/fixtures/gen-known-phrase.mjs
 */
import { readFileSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const EXPECTED_PHRASE = "what time is it";
const dir = dirname(fileURLToPath(import.meta.url));
const wavPath = join(dir, "known-phrase.wav");

const wav = readFileSync(wavPath);
if (wav.length < 1000 || wav.toString("ascii", 0, 4) !== "RIFF") {
  throw new Error(
    `${wavPath} is not a valid WAV (replace the fixture, then re-embed via gen-known-phrase.mjs)`,
  );
}
const dataUrl = `data:audio/wav;base64,${wav.toString("base64")}`;
const tsOut = join(dir, "known-phrase.ts");
writeFileSync(
  tsOut,
  `// AUTO-GENERATED by gen-known-phrase.mjs from known-phrase.wav — do not edit.\n` +
    `// Real-speech 16 kHz mono clip of EXPECTED_PHRASE,\n` +
    `// embedded as a data: URL so it bundles with no asset pipeline.\n` +
    `export const EXPECTED_PHRASE = ${JSON.stringify(EXPECTED_PHRASE)};\n` +
    `export const KNOWN_PHRASE_WAV_DATA_URL =\n  ${JSON.stringify(dataUrl)};\n`,
);
console.log(
  `wrote ${tsOut} (${wav.length} bytes wav, phrase: "${EXPECTED_PHRASE}")`,
);
