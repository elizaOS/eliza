function hasFunctionDeclaration(source, name) {
  return new RegExp(`\\b${name}\\s*\\(`).test(source);
}

function hasFunctionDefinition(source, name) {
  return new RegExp(`(?:^|\\n)[^\\n;{}]*\\b${name}\\s*\\(`).test(source);
}

function inspectPreparedOmnivoiceSurface({ graftRoot }) {
  const srcRoot = path.join(graftRoot, "src");
  const headerPath = path.join(srcRoot, "omnivoice.h");
  const implPath = path.join(srcRoot, "omnivoice.cpp");
  const adapterPath = path.join(srcRoot, "eliza-inference-ffi.cpp");
  for (const file of [headerPath, implPath, adapterPath]) {
    if (!fs.existsSync(file)) {
      throw new Error(
        `[omnivoice-fuse] prepared graft missing ${file}; refusing stub-only fusion`,
      );
    }
  }

  const header = fs.readFileSync(headerPath, "utf8");
  const impl = fs.readFileSync(implPath, "utf8");
  const adapter = fs.readFileSync(adapterPath, "utf8");
  const missingHeaderSymbols = REQUIRED_PUBLIC_OMNIVOICE_SYMBOLS.filter(
    (symbol) => !hasFunctionDeclaration(header, symbol),
  );
  if (missingHeaderSymbols.length > 0) {
    throw new Error(
      `[omnivoice-fuse] prepared omnivoice.h missing required public symbol(s): ${missingHeaderSymbols.join(", ")}. Pin is not compatible with libelizainference ABI v1.`,
    );
  }

  const missingImplSymbols = REQUIRED_PUBLIC_OMNIVOICE_SYMBOLS.filter(
    (symbol) => !hasFunctionDefinition(impl, symbol),
  );
  if (missingImplSymbols.length > 0) {
    throw new Error(
      `[omnivoice-fuse] prepared omnivoice.cpp missing required implementation symbol(s): ${missingImplSymbols.join(", ")}. Refusing to build a header-only or stub-only graft.`,
    );
  }

  const adapterRequiredCalls = [
    "ov_init_default_params",
    "ov_init",
    "ov_free",
    "ov_tts_default_params",
    "ov_synthesize",
    "ov_audio_free",
    "ov_last_error",
  ];
  const missingAdapterCalls = adapterRequiredCalls.filter(
    (symbol) => !hasFunctionDeclaration(adapter, symbol),
  );
  if (missingAdapterCalls.length > 0) {
    throw new Error(
      `[omnivoice-fuse] generated libelizainference adapter is not wired to OmniVoice call(s): ${missingAdapterCalls.join(", ")}`,
    );
  }

  return {
    header: path.relative(graftRoot, headerPath),
    implementation: path.relative(graftRoot, implPath),
    adapter: path.relative(graftRoot, adapterPath),
    requiredPublicSymbols: [...REQUIRED_PUBLIC_OMNIVOICE_SYMBOLS],
  };
}

function applyMiladyGgmlCompatibility({ graftRoot, commit }) {
  const dacPath = path.join(graftRoot, "src", "dac-decoder.h");
  let source = fs.readFileSync(dacPath, "utf8");
  const loadCtw = `// Load ConvTranspose1d weights in milady ggml's native kernel layout.
// Source-side layout is (IC, OC, K), represented by ggml as ne=(K, OC, IC).
// milady ggml already ships GGML_OP_CONV_TRANSPOSE_1D for CPU/CUDA/Metal/
// Vulkan, while omnivoice's fork used a private col2im_1d op. Keeping the
// native layout lets the fused build share the patched milady ggml without
// adding a second custom op.
static void dac_load_ctw(struct ggml_tensor * dst, const GGUFModel & gf, const std::string & name) {
    struct ggml_tensor * mt = ggml_get_tensor(gf.meta, name.c_str());
    if (!mt) {
        ov_throw("[DAC] tensor '%s' not found (conv_t1 weight)", name.c_str());
    }
    GGML_ASSERT(dst->type == GGML_TYPE_F16);

    const int    K  = (int) mt->ne[0];
    const int    OC = (int) mt->ne[1];
    const int    IC = (int) mt->ne[2];
    const size_t n  = (size_t) IC * (size_t) OC * (size_t) K;

    GGML_ASSERT(dst->ne[0] == K && dst->ne[1] == OC && dst->ne[2] == IC);

    const void * raw = gf_get_data(gf, name.c_str());
    if (mt->type == GGML_TYPE_F16) {
        ggml_backend_tensor_set(dst, raw, 0, n * sizeof(ggml_fp16_t));
        return;
    }

    std::vector<float> f32(n);
    if (mt->type == GGML_TYPE_F32) {
        memcpy(f32.data(), raw, n * sizeof(float));
    } else if (mt->type == GGML_TYPE_BF16) {
        const uint16_t * p = (const uint16_t *) raw;
        for (size_t i = 0; i < n; i++) {
            f32[i] = ggml_bf16_to_fp32(*(const ggml_bf16_t *) &p[i]);
        }
    } else {
        const struct ggml_type_traits * tr = ggml_get_type_traits(mt->type);
        if (!tr || !tr->to_float) {
            ov_throw("[DAC] unsupported conv_t1 weight type %s for '%s'", ggml_type_name(mt->type), name.c_str());
        }
        tr->to_float(raw, f32.data(), (int64_t) n);
    }

    std::vector<ggml_fp16_t> packed(n);
    for (size_t i = 0; i < n; i++) {
        packed[i] = ggml_fp32_to_fp16(f32[i]);
    }
    ggml_backend_tensor_set(dst, packed.data(), 0, n * sizeof(ggml_fp16_t));
}

`;
  source = replaceBetween(
    source,
    "// Pre-permute the ConvTranspose1d weight",
    "// Allocate one snake pair",
    loadCtw,
  );

  const convT = `// ConvTranspose1d using milady ggml's native GGML_OP_CONV_TRANSPOSE_1D.
// The upstream omnivoice fork used a private col2im_1d op with padding.
// milady's native op requires p0=0, so we crop pad samples from both ends
// and then apply output_pad to match the original length contract.
static struct ggml_tensor * dac_conv_t1d(struct ggml_context * ctx,
                                         struct ggml_tensor *  w,
                                         struct ggml_tensor *  b,
                                         struct ggml_tensor *  x,
                                         int                   stride,
                                         int                   pad,
                                         int                   oc,
                                         int                   output_pad) {
    struct ggml_tensor * y = ggml_conv_transpose_1d(ctx, w, x, stride, 0, 1);
    y = ggml_reshape_2d(ctx, y, y->ne[0], y->ne[1]);

    if (pad > 0) {
        const int64_t cropped = y->ne[0] - 2 * (int64_t) pad;
        GGML_ASSERT(cropped > 0);
        GGML_ASSERT(y->ne[1] == oc);
        y = ggml_view_2d(ctx, y, cropped, y->ne[1], y->nb[1], (size_t) pad * y->nb[0]);
        y = ggml_cont(ctx, y);
    }

    if (output_pad > 0) {
        y = ggml_pad(ctx, y, output_pad, 0, 0, 0);
    }

    if (b) {
        struct ggml_tensor * b2d = ggml_reshape_2d(ctx, b, 1, b->ne[0]);
        y                        = ggml_add(ctx, y, b2d);
    }
    return y;
}

`;
  source = replaceBetween(
    source,
    "// ConvTranspose1d via GEMM + col2im_1d",
    "// Residual unit forward:",
    convT,
  );

  source = source.replace(
    "b.ctw = ggml_new_tensor_2d(ctx, GGML_TYPE_F16, b.in_ch, b.kernel * b.out_ch);",
    "b.ctw = ggml_new_tensor_3d(ctx, GGML_TYPE_F16, b.kernel, b.out_ch, b.in_ch);",
  );
  source = source.replace(
    "struct ggml_tensor * ctw;  // [IC, K*OC] bf16",
    "struct ggml_tensor * ctw;  // [K, OC, IC] bf16",
  );
  fs.writeFileSync(dacPath, source, "utf8");

  fs.writeFileSync(
    path.join(graftRoot, "src", "version.h"),
    `#pragma once\n#define OMNIVOICE_VERSION "${commit.slice(0, 12)} (milady-fused)"\n`,
    "utf8",
  );
  writeMiladyFfiAdapter({ graftRoot, commit });
}

// Ensure a clone of omnivoice.cpp at OMNIVOICE_REF lives under
// <cacheRoot>/omnivoice.cpp. Returns the resolved HEAD commit.
function ensureOmnivoiceCheckout(cacheRoot, ref) {
  const checkoutDir = path.join(cacheRoot, "omnivoice.cpp");
  if (fs.existsSync(path.join(checkoutDir, ".git"))) {
    run("git", ["fetch", "--depth=1", "origin", ref], { cwd: checkoutDir });
    run("git", ["checkout", "FETCH_HEAD"], { cwd: checkoutDir });
  } else {
    fs.mkdirSync(cacheRoot, { recursive: true });
    // Some refs (raw commit hashes) don't work with `--branch`. Try the
    // tag/branch path first, then fall back to a full clone + checkout
    // by hash. Either way, the commit pin must resolve.
    const looksLikeHash = /^[0-9a-f]{7,40}$/i.test(ref);
    if (looksLikeHash) {
      run("git", ["clone", OMNIVOICE_REPO, checkoutDir]);
      run("git", ["checkout", ref], { cwd: checkoutDir });
    } else {
      run("git", [
        "clone",
        "--depth=1",
        "--branch",
        ref,
        OMNIVOICE_REPO,
        checkoutDir,
      ]);
    }
  }
  const head = run("git", ["rev-parse", "HEAD"], {
    cwd: checkoutDir,
    capture: true,
  });
  return { checkoutDir, head };
}

// Read the omnivoice ggml submodule's recorded commit from the parent
// repo's index, without actually fetching the submodule contents. This
// is the value we explicitly DO NOT use in the fused build — we record
// it for the manifest and verifier.
function readOmnivoiceGgmlSubmoduleCommit(checkoutDir) {
  const result = spawnSync("git", ["ls-tree", "HEAD", "ggml"], {
    cwd: checkoutDir,
    encoding: "utf8",
    stdio: ["ignore", "pipe", "pipe"],
  });
  if (result.status !== 0) return null;
  const match = /^160000\s+commit\s+([0-9a-f]{40})/.exec(
    result.stdout || "",
  );
  return match ? match[1] : null;
}

// Apply every `omnivoice-fuse/patches/*.patch` to the merged tree, in
// lexical order. Hard error on any failure — there is no fallback.
function applyPatches({ patchesDir, llamaCppRoot }) {
  if (!fs.existsSync(patchesDir)) return [];
  const patches = fs
    .readdirSync(patchesDir)
    .filter((name) => name.endsWith(".patch"))
    .sort();
  const applied = [];
  for (const name of patches) {
    const patchPath = path.join(patchesDir, name);
    const sentinel = path.join(
      llamaCppRoot,
      ".omnivoice-fuse-patch-applied",
      name,
    );
    if (fs.existsSync(sentinel)) {
      applied.push({ name, status: "already-applied" });
      continue;
    }
    run("git", ["apply", "--check", patchPath], { cwd: llamaCppRoot });
    run("git", ["apply", patchPath], { cwd: llamaCppRoot });
    fs.mkdirSync(path.dirname(sentinel), { recursive: true });
    fs.writeFileSync(sentinel, `${new Date().toISOString()}\n`);
    applied.push({ name, status: "applied" });
  }
  return applied;
}

/**
 * Main entry. Performs the full prepare phase. All errors propagate.
 */
export function prepareOmnivoiceFusion({
  cacheRoot,
  llamaCppRoot,
  omnivoiceRef = OMNIVOICE_REF,
}) {
  if (!cacheRoot) throw new Error("[omnivoice-fuse] cacheRoot is required");
  if (!llamaCppRoot)
    throw new Error("[omnivoice-fuse] llamaCppRoot is required");
  if (!fs.existsSync(path.join(llamaCppRoot, "CMakeLists.txt"))) {
    throw new Error(
      `[omnivoice-fuse] llamaCppRoot=${llamaCppRoot} missing CMakeLists.txt; clone the fork first`,
    );
  }
  if (!fs.existsSync(path.join(llamaCppRoot, "ggml", "CMakeLists.txt"))) {
    throw new Error(
      `[omnivoice-fuse] llamaCppRoot=${llamaCppRoot} missing ggml/CMakeLists.txt; the milady ggml is required for fusion`,
    );
  }

  const { checkoutDir, head } = ensureOmnivoiceCheckout(
    cacheRoot,
    omnivoiceRef,
  );
  const ggmlSubmoduleCommit = readOmnivoiceGgmlSubmoduleCommit(checkoutDir);

  // Validate the omnivoice checkout's surface. Missing pieces => hard
  // error. The runbook requires the operator to investigate, not the
  // build to silently degrade.
  const requiredDirs = ["src", "tools"];
  for (const dir of requiredDirs) {
    const full = path.join(checkoutDir, dir);
    if (!fs.existsSync(full) || !fs.statSync(full).isDirectory()) {
      throw new Error(
        `[omnivoice-fuse] omnivoice checkout at ${checkoutDir} missing required directory '${dir}' — pin ${head} is not usable`,
      );
    }
  }
  // The presence of an entry point we know exists in the pinned commit
  // (and is referenced by every fused build) — sanity check the pin.
  const sentinelHeader = path.join(checkoutDir, "src", "omnivoice.h");
  if (!fs.existsSync(sentinelHeader)) {
    throw new Error(
      `[omnivoice-fuse] omnivoice pin ${head} is missing src/omnivoice.h; refusing to graft an unknown layout`,
    );
  }

  // Stage the merged tree under <llamaCppRoot>/omnivoice/.
  const graftRoot = path.join(llamaCppRoot, OMNIVOICE_GRAFT_SUBDIR);
  // If a previous run left an aborted graft behind, blow it away. The
  // git checkout itself remains untouched — we only own the graft
  // subdirectory.
  fs.rmSync(graftRoot, { recursive: true, force: true });
  fs.mkdirSync(graftRoot, { recursive: true });

  for (const subdir of ["src", "tools"]) {
    copyDirRecursive(
      path.join(checkoutDir, subdir),
      path.join(graftRoot, subdir),
    );
  }
  applyMiladyGgmlCompatibility({ graftRoot, commit: head });
  const sourceSurface = inspectPreparedOmnivoiceSurface({ graftRoot });

  // examples/ is data-only (audio prompts, sample text). Copy on a
  // best-effort basis — the build does not depend on these files. We
  // include them so a developer running llama-omnivoice-server locally
  // has the same demo asset paths the omnivoice CLI tools document.
  const examplesSrc = path.join(checkoutDir, "examples");
  if (fs.existsSync(examplesSrc)) {
    copyDirRecursive(examplesSrc, path.join(graftRoot, "examples"));
  }

  // Hard guarantee: we did NOT bring omnivoice's ggml/ along. The
  // README's "graft, not submodule swap" strategy depends on this.
  const strayGgml = path.join(graftRoot, "ggml");
  if (fs.existsSync(strayGgml)) {
    throw new Error(
      `[omnivoice-fuse] graft staging produced a stray ${strayGgml} — refusing to continue (would create two ggml trees)`,
    );
  }

  // Apply any reconciliation patches keyed to specific drifts.
  const patchesDir = new URL("./patches/", import.meta.url).pathname;
  const appliedPatches = applyPatches({ patchesDir, llamaCppRoot });

  // Count source files actually grafted, for the manifest.
  let sourceCount = 0;
  const stack = [graftRoot];
  while (stack.length > 0) {
    const dir = stack.pop();
    for (const entry of fs.readdirSync(dir, { withFileTypes: true })) {
      const full = path.join(dir, entry.name);
      if (entry.isDirectory()) {
        stack.push(full);
      } else if (/\.(c|cc|cpp|cxx|h|hpp)$/.test(entry.name)) {
        sourceCount += 1;
      }
    }
  }

  return {
    commit: head,
    ref: omnivoiceRef,
    ggmlSubmoduleCommit,
    graftRoot,
    sourceCount,
    sourceSurface,
    appliedPatches,
  };
}
